def get_date_range(period_type):
    """
    –í—ã—á–∏—Å–ª—è–µ—Ç –¥–∞—Ç—ã –Ω–∞—á–∞–ª–∞ –∏ –∫–æ–Ω—Ü–∞ –ø–µ—Ä–∏–æ–¥–∞ –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ç–∏–ø–∞ –ø–µ—Ä–∏–æ–¥–∞.
    
    Args:
        period_type (str): –¢–∏–ø –ø–µ—Ä–∏–æ–¥–∞ ('day', 'week', 'month', 'year')
        
    Returns:
        tuple: –ö–æ—Ä—Ç–µ–∂ —Å –Ω–∞—á–∞–ª—å–Ω–æ–π –∏ –∫–æ–Ω–µ—á–Ω–æ–π –¥–∞—Ç–∞–º–∏ –≤ —Ñ–æ—Ä–º–∞—Ç–µ 'YYYY-MM-DD'
    """
    today = datetime.date.today()
    
    if period_type == 'day':
        start_date = today
        end_date = today
    elif period_type == 'week':
        start_date = today - datetime.timedelta(days=today.weekday())
        end_date = start_date + datetime.timedelta(days=6)
    elif period_type == 'month':
        start_date = today.replace(day=1)
        last_day = calendar.monthrange(today.year, today.month)[1]
        end_date = today.replace(day=last_day)
    elif period_type == 'year':
        start_date = today.replace(month=1, day=1)
        end_date = today.replace(month=12, day=31)
    else:
        # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–µ–∫—É—â–∏–π –º–µ—Å—è—Ü
        start_date = today.replace(day=1)
        last_day = calendar.monthrange(today.year, today.month)[1]
        end_date = today.replace(day=last_day)
    
    return start_date.strftime("%Y-%m-%d"), end_date.strftime("%Y-%m-%d")

