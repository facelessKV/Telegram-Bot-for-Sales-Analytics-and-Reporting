def get_sales_data(start_date, end_date):
    """
    –ü–æ–ª—É—á–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –æ –ø—Ä–æ–¥–∞–∂–∞—Ö –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –∑–∞ —É–∫–∞–∑–∞–Ω–Ω—ã–π –ø–µ—Ä–∏–æ–¥.
    
    Args:
        start_date (str): –ù–∞—á–∞–ª—å–Ω–∞—è –¥–∞—Ç–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ 'YYYY-MM-DD'
        end_date (str): –ö–æ–Ω–µ—á–Ω–∞—è –¥–∞—Ç–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ 'YYYY-MM-DD'
        
    Returns:
        pandas.DataFrame: DataFrame —Å –¥–∞–Ω–Ω—ã–º–∏ –æ –ø—Ä–æ–¥–∞–∂–∞—Ö
    """
    conn = sqlite3.connect('analytics.db')
    
    query = f"""
    SELECT 
        product_name,
        SUM(amount) as total_amount,
        date
    FROM 
        sales
    WHERE 
        date BETWEEN '{start_date}' AND '{end_date}'
    GROUP BY 
        product_name, date
    ORDER BY 
        date
    """
    # –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ: —Å—É–º–º–∞ (total_amount) —É–∂–µ –≤ –≥—Ä–∏–≤–Ω–∞—Ö
    
    df = pd.read_sql_query(query, conn)
    conn.close()
    
    return df

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö –æ–± –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∑–∞ –ø–µ—Ä–∏–æ–¥
def get_user_activity_data(start_date, end_date):
    """
    –ü–æ–ª—É—á–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –æ–± –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –∏–∑ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö –∑–∞ —É–∫–∞–∑–∞–Ω–Ω—ã–π –ø–µ—Ä–∏–æ–¥.
    
    Args:
        start_date (str): –ù–∞—á–∞–ª—å–Ω–∞—è –¥–∞—Ç–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ 'YYYY-MM-DD'
        end_date (str): –ö–æ–Ω–µ—á–Ω–∞—è –¥–∞—Ç–∞ –≤ —Ñ–æ—Ä–º–∞—Ç–µ 'YYYY-MM-DD'
        
    Returns:
        pandas.DataFrame: DataFrame —Å –¥–∞–Ω–Ω—ã–º–∏ –æ–± –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    """
    conn = sqlite3.connect('analytics.db')
    
    query = f"""
    SELECT 
        ua.user_id,
        u.username,
        ua.action_type,
        COUNT(*) as action_count,
        ua.action_date
    FROM 
        user_activity ua
    JOIN 
        users u ON ua.user_id = u.user_id
    WHERE 
        ua.action_date BETWEEN '{start_date} 00:00:00' AND '{end_date} 23:59:59'
    GROUP BY 
        ua.user_id, ua.action_type, SUBSTR(ua.action_date, 1, 10)
    ORDER BY 
        ua.action_date
    """
    
    df = pd.read_sql_query(query, conn)
    conn.close()
    
    return df

